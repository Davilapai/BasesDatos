--  PAIS
CREATE TABLE PAIS (
    ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_PAIS VARCHAR2(100) NOT NULL,

    CONSTRAINT PK_PAIS PRIMARY KEY (ID),

    CONSTRAINT UQ_PAIS_NOMBRE UNIQUE (NOMBRE_PAIS)
);

--  CIUDAD
CREATE TABLE CIUDAD (
    ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_CIUDAD VARCHAR2(100) NOT NULL,
    ID_PAIS NUMBER(10,0) NOT NULL,

    CONSTRAINT PK_CIUDAD PRIMARY KEY (ID),

    CONSTRAINT UQ_CIUDAD_NOMBRE UNIQUE (NOMBRE_CIUDAD),

    CONSTRAINT FK_CIUDAD_PAIS FOREIGN KEY (ID_PAIS) REFERENCES PAIS (ID)
);

--  ESTADIO
CREATE TABLE ESTADIO (
    ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_ESTADIO VARCHAR2(100) NOT NULL,
    CAPACIDAD NUMBER(10,0) DEFAULT 0 NOT NULL,
    ID_CIUDAD NUMBER(10,0) NOT NULL,

    CONSTRAINT PK_ESTADIO PRIMARY KEY (ID),

    CONSTRAINT UQ_ESTADIO_NOMBRE UNIQUE (NOMBRE_ESTADIO),

    CONSTRAINT CK_ESTADIO_CAPACIDAD CHECK (CAPACIDAD >= 0),

    CONSTRAINT FK_ESTADIO_CIUDAD FOREIGN KEY (ID_CIUDAD) REFERENCES CIUDAD (ID)
);

--  CONFEDERACION
CREATE TABLE CONFEDERACION (
    ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_CONFEDERACION VARCHAR2(100) NOT NULL,

    CONSTRAINT PK_CONFEDERACION PRIMARY KEY (ID),

    CONSTRAINT UQ_CONFEDERACION_NOMBRE UNIQUE (NOMBRE_CONFEDERACION)
);

--  SELECCION
CREATE TABLE SELECCION (
    ID NUMBER(10,0) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE_SELECCION VARCHAR2(100) NOT NULL,
    ID_CONFEDERACION NUMBER(10,0)  NOT NULL,

    CONSTRAINT PK_SELECCION PRIMARY KEY (ID),

    CONSTRAINT UQ_SELECCION_NOMBRE UNIQUE (NOMBRE_SELECCION),

    CONSTRAINT FK_SELECCION_CONF FOREIGN KEY (ID_CONFEDERACION) REFERENCES CONFEDERACION (ID)
);

--  PARTIDO
CREATE TABLE PARTIDO (
    ID NUMBER(10,0)  GENERATED BY DEFAULT AS IDENTITY,
    ID_ESTADIO NUMBER(10,0) NOT NULL,
    FECHA DATE DEFAULT SYSDATE NOT NULL,
    FASE VARCHAR2(20) NOT NULL,
    ID_SELECCION_LOCAL NUMBER(10,0) NOT NULL,
    ID_SELECCION_VISITANTE NUMBER(10,0) NOT NULL,
    GOLES_LOCAL NUMBER(3,0) DEFAULT 0 NOT NULL,
    GOLES_VISITANTE NUMBER(3,0) DEFAULT 0 NOT NULL,

    CONSTRAINT PK_PARTIDO PRIMARY KEY (ID),

    CONSTRAINT FK_PARTIDO_ESTADIO FOREIGN KEY (ID_ESTADIO) REFERENCES ESTADIO (ID),
    CONSTRAINT FK_PARTIDO_LOCAL FOREIGN KEY (ID_SELECCION_LOCAL) REFERENCES SELECCION (ID),
    CONSTRAINT FK_PARTIDO_VISIT FOREIGN KEY (ID_SELECCION_VISITANTE) REFERENCES SELECCION (ID),

    CONSTRAINT CK_PARTIDO_FASE CHECK (FASE IN ('GRUPOS', 'OCTAVOS', 'CUARTOS', 'SEMIFINAL', 'FINAL')),
    CONSTRAINT CK_PARTIDO_GOLES_L CHECK (GOLES_LOCAL >= 0),
    CONSTRAINT CK_PARTIDO_GOLES_V CHECK (GOLES_VISITANTE >= 0),
    CONSTRAINT CK_PARTIDO_NO_MISMO CHECK (ID_SELECCION_LOCAL != ID_SELECCION_VISITANTE) -- Tambien funciona <>
);

