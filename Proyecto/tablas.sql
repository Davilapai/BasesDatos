-- Querys -- 
-- 1. Listar todos los partidos mostrando:
-- Nombre del pais, ciudad, estadio, fecha, fase, seleccion local, seleccion visitante, goles local y goles visitante.
-- Ordenado por pais, ciudad, estadio y fecha.
SELECT 
    P.NOMBRE_PAIS, 
    C.NOMBRE_CIUDAD, 
    E.NOMBRE_ESTADIO, 
    PA.FECHA, 
    PA.FASE, 
    SL.NOMBRE_SELECCION AS SELECCION_LOCAL, 
    SV.NOMBRE_SELECCION AS SELECCION_VISITANTE, 
    PA.GOLES_LOCAL, 
    PA.GOLES_VISITANTE

FROM PARTIDO PA
JOIN ESTADIO E ON PA.ID_ESTADIO = E.ID
JOIN CIUDAD C ON E.ID_CIUDAD = C.ID
JOIN PAIS P ON C.ID_PAIS = P.ID 
JOIN SELECCION SL ON PA.ID_SELECCION_LOCAL = SL.ID
JOIN SELECCION SV ON PA.ID_SELECCION_VISITANTE = SV.ID
ORDER BY P.NOMBRE_PAIS, C.NOMBRE_CIUDAD, E.NOMBRE_ESTADIO, PA.FECHA;

SELECT ID, NOMBRE_ESTADIO FROM ESTADIO;

-- 2. Contar cantidad de partidos por ciudad
-- Listar ciudad, estadio, cantidad de partidos

SELECT C.NOMBRE_CIUDAD, E.NOMBRE_ESTADIO, COUNT(*) AS CANTIDAD_PARTIDOS
FROM PARTIDO PA
JOIN ESTADIO E ON PA.ID_ESTADIO = E.ID
JOIN CIUDAD C ON E.ID_CIUDAD = C.ID
GROUP BY C.NOMBRE_CIUDAD, E.NOMBRE_ESTADIO;

-- 3. Cuantos goles ha anotado cada seleccion en el torneo
-- (Tener en cuenta goles como local y visitante)
-- Listar: Nombre de la seleccion, total de goles
-- En la ultima fila mostrar el total general.ALTER
SELECT S.NOMBRE_SELECCION,
        SUM(CASE
                WHEN S.ID = PA.ID_SELECCION_LOCAL THEN PA.GOLES_LOCAL
                WHEN S.ID = PA.ID_SELECCION_VISITANTE THEN PA.GOLES_VISITANTE
            END
        ) AS TOTAL_GOLES
     
FROM SELECCION S
JOIN PARTIDO PA ON S.ID = PA.ID_SELECCION_LOCAL OR S.ID = PA.ID_SELECCION_VISITANTE
GROUP BY S.NOMBRE_SELECCION;

-- 4. Cuantos goles se anotan por país mensualmente
-- Listar: Pais, año, mes, total de goles 
-- Los paises sin partidos deben aparecer en 0.
SELECT P.NOMBRE_PAIS,
        EXTRACT(YEAR FROM PA.FECHA) AS ANIO,
        EXTRACT(MONTH FROM PA.FECHA) AS MES,
        NVL(SUM(PA.GOLES_LOCAL + PA.GOLES_VISITANTE), 0) AS TOTAL_GOLES
FROM PAIS P
LEFT JOIN CIUDAD C ON C.ID_PAIS = P.ID
LEFT JOIN ESTADIO E ON E.ID_CIUDAD = C.ID
LEFT JOIN PARTIDO PA ON PA.ID_ESTADIO = E.ID

GROUP BY P.NOMBRE_PAIS, EXTRACT(YEAR FROM PA.FECHA), EXTRACT(MONTH FROM PA.FECHA);